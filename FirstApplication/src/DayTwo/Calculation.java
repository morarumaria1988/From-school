
package DayTwo;

 public class Calculation {
        
        public double add(double a, double b) {
            return (a + b);
        }

        public double substraction(double a, double b) {
            return (a - b);
        }

        public double multiplication(double a, double b) {
            return (a * b);
        }

        public double division(double a, double b) {
            return (a / b);
        }
    }